# 11. WAP a calculate total money in a piggi bank.

a = int(input("Enter the number of 10 rupee coin: "))
b = int(input("Enter the number of 5 rupeecoin: "))
c = int(input("Enter the number of 2 rupee coin: "))
d = int(input("Enter the number of 1 rupee coin: "))

total_money = (10 * a) + (5 * b) + (2 * c) + (1 * d)

print("Total money in piggi bank: ", total_money, "rupee")


