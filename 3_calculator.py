# Design a calculator by using arithmatic operation in python programming.

a = int(input("Enter the 1st number: "))
b = int(input("Enter the 2nd number: "))

sum = a + b
sub = a - b
mul = a * b
div = a / b

print("Addition of the given number: ", sum)
print("Subtraction of the given number: ", sub)
print("Multiplication of given number: ", mul)
print("Division of given number: ", div)


# Output:
# Enter the 1st number: 15
# Enter the 2nd number: 4
# Addition of the given number:  19
# Subtraction of the given number:  11
# Multiplication of given number:  60
# Division of given number:  3.75