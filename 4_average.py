# 4. WAP to calculate average of four number

a1 = int(input("Enter 1st number: "))
a2 = int(input("Enter 2nd number: "))
a3 = int(input("Enter 3rd number: "))
a4 = int(input("Enter 4th number: "))

avg = (a1 + a2 + a3 + a4) / 4

print("Average of 4 number: ", avg)


# Output: 
# Enter 1st number: 2
# Enter 2nd number: 4
# Enter 3rd number: 6
# Enter 4th number: 8
# Average of 4 number:  5.0