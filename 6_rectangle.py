length = int(input("Enter the length: "))
width = int(input("Enter the width: "))

area = length * width
peri = 2 * (length + width)

print("Area of rectangle: ", area)
print("Perimeter of rectangle: ", peri)


# Output:
# Enter the length: 15
# Enter the width: 10
# Area of rectangle:  150
# Perimeter of rectangle:  50